package Git04.SimpleObjects.Task2;

public class Solution {
    public static class Test2{
        int x,y;

        public Test2(int x, int y) {
            this.x = x;
            this.y = y;
        }

        public Test2() {
            x=0;
            y=0;
        }

        public int getX() {
            return x;
        }

        public void setX(int x) {
            this.x = x;
        }

        public int getY() {
            return y;
        }

        public void setY(int y) {
            this.y = y;
        }
    }
}
